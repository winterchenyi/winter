  `LAST_UPD_DATE` date DEFAULT NULL COMMENT '最后修改时间',
  `LAST_ACCOUNT_ID` varchar(32) DEFAULT NULL COMMENT '最后操作用户',
  PRIMARY KEY (`ID`,`LOGIN_NAME`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

